package com.hamitmizrak;

public class _5_1_Operators {

    public static void main(String[] args) {
        // Operators (+,-,*,/,%)
        int number1=25;
        int number2=3;
        // +
        int topla=number1+number2;
        System.out.println("TOPLA: "+topla);

        // -
        int cikar=(number1-number2);
        System.out.println("ÇIKARMA: "+cikar);

        // *
        int carp=(number1*number2);
        System.out.println("ÇARPMA: "+carp);

        // /
        int bol=(number1/number2);
        System.out.println("BÖL: "+bol);

        // %
        int kalan=(number1%number2);
        System.out.println("KALAN: "+kalan);

        // Ödev: Yukarıdaki öreneğe benzer olarak
        /*
        number1=18
        number2=6
        bu sayılarda (+ - * / % ) işlemleri consolda yazdıralım ?
        * */
    }
}
